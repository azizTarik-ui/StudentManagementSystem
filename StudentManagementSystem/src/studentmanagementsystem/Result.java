    private void GOButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GOButtonActionPerformed
        
        String dept=null,section=null;
        int year,id;
        
        dept=Dept.getText();
        //id=Integer.parseInt(ID.getText());
        year=Integer.parseInt(Year.getText());
        section=Sem.getText();
        System.out.println(dept);
        
        try
        {
             Class.forName("com.mysql.jdbc.Driver");
             con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/studentmanagement","root","");
             pst=(PreparedStatement) con.prepareStatement("select * from userinfo where Dept=? and Class=? and Semester=? and Shift=?");
             pst.setString(1, dept);
            pst.setInt(2,year);
            pst.setString(3,section);
            pst.setString(4,StudentLogin.shift);
            rs=pst.executeQuery();
            
            //show table codes
            Object rowdata[]=new Object[6];
            
            //travarse database
            while(rs.next())
            {
                rowdata[0]=rs.getInt("ID");
                rowdata[1]=rs.getString("Name");
                rowdata[2]=rs.getString("Class");
                rowdata[3]=rs.getString("Semester");
                rowdata[4]=rs.getString("CGPA");
                //rowdata[5]=rs.getString("Dept");
                model.addRow(rowdata);
            }
            
            
        }
        catch (SQLException ex) {
            Logger.getLogger(AllStudentShow.class.getName()).log(Level.SEVERE, null, ex);
         }
        
         catch (ClassNotFoundException ex) {
            Logger.getLogger(AllStudentShow.class.getName()).log(Level.SEVERE, null, ex);
         }
        
    }//GEN-LAST:event_GOButtonActionPerformed

